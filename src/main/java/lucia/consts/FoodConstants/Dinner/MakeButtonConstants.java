package main.java.lucia.consts.FoodConstants.Dinner;

/**
 * Constants for the Make Button on the Dinner Sides Pane
 */
public class MakeButtonConstants {
    private int layoutX;
    private int layoutY;
    private int sizeX;
    private int sizeY;

    public MakeButtonConstants() {
        layoutX = 430;
        layoutY = 680;
        sizeX = 185;
        sizeY = 87;
    }

    public int getLayoutX() {
        return layoutX;
    }

    public int getLayoutY() {
        return layoutY;
    }

    public int getSizeX() {
        return sizeX;
    }

    public int getSizeY() {
        return sizeY;
    }
}
